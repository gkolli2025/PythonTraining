'''
Assignment Operators:
These operators are used to assign
    1. value to variable            x = 10
    2. variable another variable    x = y
    3. expression to a variable  x = y+z+4

+=  -----> x+=4 --> x = x+4
-= -----> x-=4 --> x = x-4
*= ------> x*=4  --> x = x*4
/=  ------> x/=4  --> x = x/4
//= -- ---> x//=4 --> x=x//4
%=  ------> x%=4 --> x = x%4
**= -------> x**=4 ---> x=x**4

'''
x=10
x+=4
print(x)

x-=6
print(x)

x*=4
print(x)

x/=4
print(x)

x//=4
print(x)

x**=3
print(x)

