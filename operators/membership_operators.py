'''
Membership operators: these operators are used to check whether an element is a member of not

1. in
2. not in
'''

x=[10,20,30,40,50]
print(10 in x)
print(15 in x)
print(10 not in x)
print(15 not in x)

emps=["Nagarjun", "Prateek", "Geetha"]
print("David" in emps)
print("Nagarjun" in emps)